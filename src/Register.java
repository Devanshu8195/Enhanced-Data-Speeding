    private void txtEmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtEmailFocusLost

        String z = txtEmail.getText();

        if(!z.equals(""))
        {
            if(!(((z.contains("gmail.com"))|| (z.contains("yahoo.com")) || (z.contains("yahoo.co.in"))|| (z.contains("gmail.co.in"))) && z.contains("@")))
            {
                lblError3.setVisible(true);
                lblError3.setText("Please insert correct email-id..!");
                lblError3.setForeground(Color.red);
                txtEmail.requestFocus();
                txtEmail.setBackground(Color.LIGHT_GRAY);
            }
            else
            {
                lblError3.setText("");
                txtEmail.setBackground(Color.white);
            }
        }

    }//GEN-LAST:event_txtEmailFocusLost


