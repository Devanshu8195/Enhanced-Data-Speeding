    private void btnContinueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinueActionPerformed

        String s1 = "";
        s1 = txtOtp.getText();
        
        if(!s1.equals(""))
        {
            JOptionPane.showMessageDialog(null, "if start otp = "+randomNum);
            if(s1.equals(String.valueOf(randomNum)))
            {
                JOptionPane.showMessageDialog(null, "if start otp inner");                
                lblError.setText("");
                txtFname.setEnabled(false);
                txtLname.setEnabled(false);
                txtEmail.setEnabled(false);
                txtContact.setEnabled(false);
                txtOtp.setEnabled(false);
                btnContinue.setEnabled(false);
                btnResendotp.setEnabled(false);
                txtUname.setEnabled(true);
                txtPass.setEnabled(true);
                txtCpass.setEnabled(true);
                btnRegister.setEnabled(true);
                btnClear.setEnabled(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Else inner");                
                lblError.setText("Your OTP is incorrecct...");
            }
        }
        else
        {
            lblError.setText("You must enter OTP number...");
        }
    }//GEN-LAST:event_btnContinueActionPerformed

