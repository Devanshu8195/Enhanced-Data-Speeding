    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed

        c = new plasmaCon();        
        String uname = null, pass = null, cpass = null;
        String insert = "";
        
        uname = txtUname.getText();
        pass = txtPass.getText();
        cpass = txtCpass.getText();
//        JOptionPane.showMessageDialog(null, "Fn = "+fn+",  Ln = "+ln+", email = "+email+",  contact = "+contact+",  uname = "+uname+",  pass = "+pass+"");
        try
        {
            
            if(!uname.equals("") && !pass.equals("") && !cpass.equals(""))
            {
                if(pass.equals(cpass))
                {
                  
    //                 JOptionPane.showMessageDialog(null, "Fn = "+fn+",  Ln = "+ln+", email = "+email+",  contact = "+contact+",  uname = "+uname+",  pass = "+pass+"");        
                    c.st = c.con.createStatement();
                    insert = "INSERT into tbl_register (FName,LName,Email_Id,ContactNo,UName,Password) values('"+fn+"','"+ln+"','"+MyEmail+"','"+contact+"','"+uname+"','"+pass+"')";
                    c.st.executeUpdate(insert);
                    JOptionPane.showMessageDialog(null, "You have registered successfully..!");
                    c.con.close();
                    clear();

                    Login l = new Login();
                    dispose();
                    l.setVisible(true);
                    
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Please insert all details..!");
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());}

    }//GEN-LAST:event_btnRegisterActionPerformed

